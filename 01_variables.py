age = 67

print(age + 10)
print(age - 10)
print(age * 10)
print(age / 3)  # обычное деление 10 / 3 = 3.3333333333333333
print(age // 3)  # целочисленное деление 10 // 3 = 3 <- отбрасывает дробную часть в результате деления
print(age % 3)  # возврат остатка от деления 10 % 3 = 1
print(age ** 2)  # возведение в степень


